namespace Grocery\Controller\Factory;


use Grocery\Controller\IndexController;
use Grocery\Service\GroceryManager;
use Interop\Container\ContainerInterface;
use Zend\ServiceManager\Factory\FactoryInterface;

class IndexControllerFactory implements FactoryInterface
{
    public function __invoke(ContainerInterface $container, $requestedName, array $options = null)
    {
        $entityManager = $container->get('doctrine.entitymanager.orm_default');
        $amenitiesManager = $container->get(GroceryManager::class);
        return new IndexController($entityManager, $amenitiesManager);
    }


}